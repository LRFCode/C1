package com.company;

public class Main
{
    public static void main(String[] args)
    {
        //Declare a String reference named hi
        String hi;

        //Assign a String to the reference hi
        hi = "hello";

        //Print out hi
        System.out.println(hi);

        //Declare a String reference named bye
        //and assign a String to it in the same line
        String bye = "goodbye";

        //Print out bye
        System.out.println(bye);
    }
}
